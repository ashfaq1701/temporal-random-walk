"""Three-way benchmark on the stratified synthetic graph.

Runs ablation_streaming for FW / NG / NG_global, REPS times each, and
prints mean ± std walks/sec, steps/sec, and NG-vs-FW speedup.

The dataset is generated by gen_synthetic_stratified.py. Walk config is
hardcoded to the cell that demonstrates the NG > FW win on this hardware
(see the generator's docstring for the rationale).

    python bench_synthetic.py [--csv synthetic_stratified.csv] [--reps 3]
"""
import argparse
import re
import statistics
import subprocess
import sys
from pathlib import Path

VARIANTS = ['full_walk', 'node_grouped', 'node_grouped_global_only']

WPN          = '500'   # A40-scale aggressive; drop to 50 for laptop sanity
MWL          = '100'   # walks already saturate; bigger mwl amortizes scheduler
NUM_BATCHES  = '1'
NUM_WINDOWS  = '1'
PICKER       = 'exponential_index'
IS_DIRECTED  = '1'
TIMESCALE    = '-1'
BLOCK_DIM    = '256'
W_THR_WARP   = '4'

THROUGHPUT_RE = re.compile(r'^Throughput:\s+([\d.eE+-]+)\s+walks/sec', re.MULTILINE)
STEPS_RE      = re.compile(r'^Steps/sec:\s+([\d.eE+-]+)\s+steps/sec',  re.MULTILINE)
AVGLEN_RE     = re.compile(r'^Final avg walk length:\s*([\d.eE+-]+)',  re.MULTILINE)


def parse_summary(s):
    return (float(THROUGHPUT_RE.search(s).group(1)),
            float(STEPS_RE.search(s).group(1)),
            float(AVGLEN_RE.search(s).group(1)))


def mean_std(xs):
    mu = statistics.mean(xs)
    sd = statistics.stdev(xs) if len(xs) > 1 else 0.0
    return mu, sd


def main():
    here = Path(__file__).resolve().parent
    ap = argparse.ArgumentParser()
    ap.add_argument('--csv', default=str(here / 'synthetic_stratified.csv'))
    ap.add_argument('--reps', type=int, default=3)
    ap.add_argument('--binary',
                    default=str(here.parent / 'build' / 'bin' / 'ablation_streaming'))
    args = ap.parse_args()

    binary = Path(args.binary)
    csv_path = Path(args.csv)
    if not binary.is_file():
        sys.exit(f'binary not found: {binary}')
    if not csv_path.is_file():
        sys.exit(f'csv not found: {csv_path}')

    print(f'# binary : {binary}')
    print(f'# csv    : {csv_path}')
    print(f'# config : wpn={WPN} mwl={MWL} nb={NUM_BATCHES} nw={NUM_WINDOWS} '
          f'picker={PICKER} dir=Forward')
    print(f'# reps   : {args.reps}')

    results = {v: [] for v in VARIANTS}
    for variant in VARIANTS:
        print(f'\n=== {variant} ===', flush=True)
        for rep in range(args.reps):
            argv = [str(binary), str(csv_path),
                    '1', PICKER, variant, IS_DIRECTED,
                    WPN, NUM_BATCHES, NUM_WINDOWS, MWL, TIMESCALE,
                    BLOCK_DIM, W_THR_WARP]
            proc = subprocess.run(argv, capture_output=True,
                                  text=True, check=False)
            if proc.returncode != 0:
                print(proc.stderr[-300:], file=sys.stderr)
                sys.exit(f'{variant} rep={rep+1} failed')
            wps, sps, avg_len = parse_summary(proc.stdout)
            results[variant].append((wps, sps, avg_len))
            print(f'  rep {rep+1}/{args.reps}  '
                  f'walks/s={wps/1e6:6.3f}M  '
                  f'steps/s={sps/1e6:6.3f}M  '
                  f'avg_len={avg_len:.3f}', flush=True)

        rs = results[variant]
        wmu, wsd = mean_std([r[0] for r in rs])
        smu, ssd = mean_std([r[1] for r in rs])
        print(f'  -- mean  walks/s={wmu/1e6:6.3f}±{wsd/1e6:.3f}M  '
              f'steps/s={smu/1e6:6.3f}±{ssd/1e6:.3f}M', flush=True)

    print()
    print('=' * 76)
    print(' Summary (positive % = variant faster than full_walk)')
    print('=' * 76)
    fw_steps = statistics.mean(r[1] for r in results['full_walk'])
    print(f' {"variant":<28} {"walks/s (M)":>15} {"steps/s (M)":>15} {"vs FW":>10}')
    print('-' * 76)
    for variant in VARIANTS:
        rs = results[variant]
        wmu, wsd = mean_std([r[0] for r in rs])
        smu, ssd = mean_std([r[1] for r in rs])
        gain = ((smu - fw_steps) / fw_steps * 100
                if variant != 'full_walk' else 0.0)
        gstr = '—' if variant == 'full_walk' else f'{gain:+6.2f}%'
        print(f' {variant:<28} '
              f'{wmu/1e6:>9.3f}±{wsd/1e6:.3f} '
              f'{smu/1e6:>9.3f}±{ssd/1e6:.3f} '
              f'{gstr:>10}')


if __name__ == '__main__':
    main()
